package task3;

public class Worker {

    String name;
    String company;
    int age;
    String phone;
    String job;
    String hopeArea;
    String hopeJob;

    public Worker(String name, String company, int age, String phone,
                  String job, String hopeArea, String hopeJob) {
        this.name = name;
        this.company = company;
        this.age = age;
        this.phone = phone;
        this.job = job;
        this.hopeArea = hopeArea;
        this.hopeJob = hopeJob;
    }
}

